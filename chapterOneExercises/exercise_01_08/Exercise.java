/*
1.08
(Area and perimeter of a circle) Write a program that displays the area and perimeter
of a circle that has a radius of 5.5 using the following formula:
					perimeter = 2 * radius * pi
					area = radius * radius * pi
*/
public class Exercise {
    public static void main(String[] args) {
        System.out.println("The perimiter of a circle with a radius of 5.5 is " + 
                (2 * 5.5 * 3.14159));  
        System.out.println("The Area of a circle with a radius of 5.5 is " + 
                (5.5 * 5.5 * 3.14159));  
    }
}
